CREATE TABLE `sensor_data` (
	`Time` datetime NOT NULL,
	`SensorID` varchar(10) NOT NULL,
	`durP1` double DEFAULT '0',
	`ratioP1` double DEFAULT '0',
	`P1` double DEFAULT '0',
	`durP2` double DEFAULT '0',
	`ratioP2` double DEFAULT '0',
	`P2` double DEFAULT '0',
	`SDS_P1` double DEFAULT '0',
	`SDS_P2` double DEFAULT '0',
	`Temp` double DEFAULT '0',
	`Humidity` double DEFAULT '0',
	`BMP_temperature` double DEFAULT '0',
	`BMP_pressure` double DEFAULT '0',
	`BME280_temperature` double DEFAULT '0',
	`BME280_humidity` double DEFAULT '0',
	`BME280_pressure` double DEFAULT '0',
	`Samples` double DEFAULT '0',
	`Min_cycle` double DEFAULT '0',
	`Max_cycle` double DEFAULT '0',
	`Signal` double DEFAULT '0',
	PRIMARY KEY (`Time`,`SensorID`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;
